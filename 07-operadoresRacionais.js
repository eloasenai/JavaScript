// operadores racionais
var x = 22;
var y = 5;

console.log("x =", x, "| y =", y);

console.log("x é igual a y? |", x == y);
console.log("x é igual a y |", x != y);

console.log("x é maior que y? |", x > y);
console.log("x é maior que y? |", x < y);
console.log("x é maior que y? |", x >= y);
console.log("x é maior que y? |", x <= y);

