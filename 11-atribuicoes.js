//incremento e decremento com valor especifico 
//operadores de atribuição aritimetica 

var x = 10 
var y = 5 

console.log("-----------")
x+= y; //x = x + y
console.log("x =", x,"| y =",y)
x-=y; //x= x - y
console.log("x =", x,"| y =",y)
x*=y; //x = x * y
console.log("x =", x,"| y =",y)
x/=y; //x = x / y
console.log("x =", x,"| y =",y)


